# Print colored messages
print_info() {
    echo -e "\033[1;38;2;137;180;250mINFO: $1\033[0m"
}

print_success() {
    echo -e "\033[1;38;2;166;227;161mSUCCESS: $1\033[0m"
}

print_warning() {
    echo -e "\033[1;38;2;249;226;175mWARNING: $1\033[0m"
}

print_error() {
    echo -e "\033[1;38;2;243;139;168mERROR: $1\033[0m"
}
